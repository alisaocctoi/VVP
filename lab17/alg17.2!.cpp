#include <iostream>
using namespace std;
int main()
{
	int N, L, a = 1, b = 0, i, j;
	cout << " введите N:";
	cin >> N;
	int* arr = new int[N];
	cout << " введте массив:";
	for (int i = 0; i < N; i++)
	{
	
		cin >> arr[i];
	}
	int* mass = new int[N];
	cout << " введите L:";
	cin >> L;
	for (int i = 0; i < N; i++)
	{
		if (arr[i] == arr[i + 1])
		{
			a++;
		}
		else
		{
			if (a > L)
			{
				mass[b] = 0;
				mass[b + 1] = arr[i + 1];
				a = 1;
				b++;
			}
			else
			{
				mass[b] = arr[i];
				b++;
			}
		}
	}
	for (int i = 0; i < b; i++)
	{
		cout << mass[i];
	}
	return 0;
}
